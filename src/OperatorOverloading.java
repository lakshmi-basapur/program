public class OperatorOverloading
{
    public static void main(String[] args)
    {
        System.out.println("hi " +" everyone");
        System.out.println("hi " +" everyone  "  +20);
        System.out.println("hi " +" everyone  " + 50 +40);
        System.out.println("hi " +" everyone  " + (50 +40));
    }
}